/**
 * allocator.h — カスタムメモリアロケータ のヘッダ
 *
 * 固定サイズのメモリプールを使い、malloc / free 相当の機能を
 * C23 で実装する。
 *
 * 学べること：
 *   - ヒープ管理の仕組み（ブロックリスト）
 *   - フラグメンテーションとコアレッシング（隣接ブロックの結合）
 *   - ポインタ演算
 *   - ファーストフィット vs ベストフィットの違い
 *   - デバッグ用のメモリリーク検出
 */

#pragma once

#include <stdbool.h>
#include <stddef.h>

/* ------------------------------------------------------------------ */
/* 定数                                                                 */
/* ------------------------------------------------------------------ */

/** アロケータが管理するメモリプールのバイト数（1 MiB） */
#define POOL_SIZE (1U * 1024U * 1024U)

/* ------------------------------------------------------------------ */
/* ブロックヘッダ構造体                                                  */
/* ------------------------------------------------------------------ */

/**
 * BlockHeader — 各メモリブロックの先頭に置くメタ情報
 *
 *  +------------------+  <-- BlockHeader (このヘッダ自体)
 *  | size             |      ユーザーが使えるバイト数
 *  | is_free          |      true なら空きブロック
 *  | next             |      次のブロックへのポインタ（NULL なら末尾）
 *  +------------------+  <-- ユーザーに返すアドレス（header + 1）
 *  | user data ...    |
 *  +------------------+
 */
typedef struct BlockHeader {
    size_t size;             /**< ユーザーデータ部分のバイト数 */
    bool is_free;            /**< 空きブロックなら true         */
    struct BlockHeader *next; /**< 次のブロック（NULL なら末尾）  */
} BlockHeader;

/* ------------------------------------------------------------------ */
/* アロケータの初期化 / 統計                                             */
/* ------------------------------------------------------------------ */

/** プールを初期化する（プログラム起動時に一度だけ呼ぶ） */
void allocator_init(void);

/** 現在の状態をデバッグ表示する */
void allocator_dump(void);

/**
 * メモリリークを検出する。
 * 解放されていないブロックがあれば標準エラーに出力し、
 * 件数を返す。0 ならリークなし。
 */
size_t allocator_check_leaks(void);

/* ------------------------------------------------------------------ */
/* ファーストフィット                                                    */
/* ------------------------------------------------------------------ */

/**
 * my_malloc — ファーストフィットでメモリを確保する
 * @param size  確保したいバイト数
 * @return      確保した領域の先頭ポインタ。失敗時は NULL。
 */
void *my_malloc(size_t size);

/**
 * my_free — my_malloc で確保した領域を解放する
 * @param ptr   解放するポインタ（NULL を渡しても安全）
 */
void my_free(void *ptr);

/* ------------------------------------------------------------------ */
/* ベストフィット                                                        */
/* ------------------------------------------------------------------ */

/**
 * my_malloc_best_fit — ベストフィットでメモリを確保する
 * @param size  確保したいバイト数
 * @return      確保した領域の先頭ポインタ。失敗時は NULL。
 */
void *my_malloc_best_fit(size_t size);
